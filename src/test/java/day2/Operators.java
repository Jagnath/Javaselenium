public class Operators {
    public static void main(String[] args) {
        int a=20;
        int b=20;      //Addition
        int add=a+b;

        int p=40;
        int q=20;      //Subtraction
        int sub=p-q;

        int c=10;
        int d=20;     //Multiplication
        int mul=c*d;

        int e=60;
        int f=20;     //Division
        int div=e/f;

        System.out.println("The addition of two digits value is " +add);
        System.out.println("The subtraction of two digits value is " +sub);
        System.out.println("The multiplication of two digits value is "+mul);
        System.out.println("The division of two digits value is "+div);




    }

}

