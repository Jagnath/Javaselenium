public class PrimitiveDataType {
    public static void main(String[] args) {
        int age;                          // variable declaration
        age=75;                          // variable initialization
        int password=123123;
        long atm_no=1234123412341234l;
        short land_no=02324;
        double balance=5000.60;
        float weight=55.40f;
        char ch='J';

        System.out.println("My age is = "+age);    // printing statement
        System.out.println("My password is = "+password);
        System.out.println("My ATM number is = "+atm_no);
        System.out.println("My age is = "+land_no);
        System.out.println("Account balance is = "+balance);
        System.out.println("My weight is = "+weight);
        System.out.println("The characters start with = "+ch);



    }

}
